package patterns_practice;

public class pattern1 {
    public static void main(String[] args) {

        for (int j = 1; j <= 5; j++) {
            for (int i = 1; i <= j; i++) {
                System.out.print(i+"");
            }
            System.out.println();
        }

    }
}
